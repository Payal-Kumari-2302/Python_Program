# Time-Zone Program

import time

# Full current time
current_time = time.strftime("%H:%M:%S")
print("Current Time:", current_time)

# Hour
hour = int(time.strftime("%H"))
print("Hour:", hour)

# Minute
minute = int(time.strftime("%M"))
print("Minute:", minute)

# Second
second = int(time.strftime("%S"))
print("Second:", second)
