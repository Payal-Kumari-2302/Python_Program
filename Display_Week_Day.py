# W.A.Python program to accept any week day number and display the corresponding week day name

day=int(input("Enter any Week Day Number:->"))

if day==1:
    print("Monday")
elif day==2:
    print("Tuesday")
elif day==3:
    print("Wednesday")
elif day==4:
    print("Thursday")
elif day==5:
    print("Friday")
elif day==6:
    print("Saturday")
elif day==7:
    print("Sunday")
else:
    print("Invalid Week Day Number")
