# Program_3
# W.A Python Program to print all escape sequences

# A. New Line (\n)
print("Payal\nKumari")

# B. Tab Space (\t)
print("Payal\tJaiswal")

# C. Vertical Tab (\v)
print("Payal\vJaiswal")

# D. Backspace (\b)
print("Ram is a box\by")

# E. Backslash (\\)
print("Payal\\Babli")

# F. Double Quote (\")
print("\"Payal\"")

# G. Single Quote (\')
print("Name\'s")

# H. Carriage Return (\r)
print("Sohan is a boy\rM")


